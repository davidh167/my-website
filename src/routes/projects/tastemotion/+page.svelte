    <h2 class="h2 text-center">TastEmotion</h2>
    <p class="text-center text-lg max-w-lg mx-auto">
        A research project conducted as part of Applied Affective Computing, exploring the relationship between taste perception and emotional responses using EEG data processed by the Emotiv headset.
    </p>

    <!-- Technologies Used -->
    <div class="mt-10">
        <h3 class="font-semibold text-lg">Technologies Used</h3>
        <ul class="list-disc list-inside text-sm mt-4">
            <li><strong>Hardware:</strong> Emotiv EEG headset, which provides preprocessed EEG data for analysis.</li>
            <li><strong>Data Processing:</strong> Python for further processing and feature extraction of the preprocessed data.</li>
            <li><strong>Analysis:</strong> Machine learning techniques to map the preprocessed EEG data to emotional states.</li>
            <li><strong>Experiment Design:</strong> Controlled environment to evaluate taste-based emotional responses.</li>
        </ul>
    </div>

    <!-- Features Section -->
    <div class="mt-10">
        <h3 class="font-semibold text-lg">Key Aspects</h3>
        <ul class="list-disc list-inside text-sm mt-4">
            <li>Data collection using the Emotiv EEG headset to record preprocessed signals while subjects tasted different stimuli.</li>
            <li>Analysis of the preprocessed EEG data to identify emotional responses related to taste perception.</li>
            <li>Comparing responses across different flavors to assess consistency and variance.</li>
            <li>Potential applications in food science, marketing, and human-computer interaction.</li>
        </ul>
    </div>

    <!-- Challenges Section -->
    <div class="mt-10">
        <h3 class="font-semibold text-lg">Challenges</h3>
        <p class="text-sm mt-2">
            One challenge was the variability in emotional responses and ensuring consistency across participants. The Emotiv headset provided preprocessed data, which required careful feature selection for accurate emotional inference. Equipment limitations, such as slow data streaming, hindered real-time analysis, making it difficult to capture continuous emotional responses at the desired rate. Calibration and external factors like electrode placement also posed challenges, requiring fine-tuning for reliable predictions.
        </p>
    </div>

    <!-- Impact Section -->
    <div class="mt-10">
        <h3 class="font-semibold text-lg">Impact</h3>
        <p class="text-sm mt-2">
            This research advances the understanding of the connection between taste and emotional responses using neurophysiological data. By utilizing preprocessed data from the Emotiv headset, we demonstrated the potential for real-time emotion recognition tied to sensory experiences. The insights gained could contribute to affective computing, personalized nutrition, and sensory-driven AI applications, offering new ways to tailor experiences in food science and marketing.
        </p>
    </div>

